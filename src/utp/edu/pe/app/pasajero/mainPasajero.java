    private void btnActualizarPasajeroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarPasajeroMouseClicked
        actualizarPasajero actualizarPasajero = new actualizarPasajero();
        actualizarPasajero.setSize(770, 700);
        actualizarPasajero.setLocation(0,0);
        
        int fila = tblPasajeros.getSelectedRow();
        String nroDocumento = tblPasajeros.getValueAt(fila, 2).toString();        
        actualizarPasajero.cargarPasajero(nroDocumento);
        
        content.removeAll();
        content.add(actualizarPasajero, BorderLayout.CENTER);
        content.revalidate();
        content.repaint();
    }//GEN-LAST:event_btnActualizarPasajeroMouseClicked

    private void btnEliminarPasajeroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarPasajeroMouseClicked
        daoPasajeros pasajeroDatos = new daoPasajeros();
        int fila = tblPasajeros.getSelectedRow();
        String msjRespuesta;        
        String nroDocumento = tblPasajeros.getValueAt(fila, 2).toString();        
        int res;
        
        res = JOptionPane.showConfirmDialog(this, "Â¿Desea eliminar el registro seleccionado", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (res == 0) {
            msjRespuesta = pasajeroDatos.eliminarPasajero(nroDocumento);
            JOptionPane.showMessageDialog(this, msjRespuesta);
            cargarPasajeros();
        }        
    }//GEN-LAST:event_btnEliminarPasajeroMouseClicked
    
    public void cargarPasajeros() {                
        DefaultTableModel modeloTabla = (DefaultTableModel) tblPasajeros.getModel();
        modeloTabla.setRowCount(0);
        
        daoPasajeros pasajeroDatos = new daoPasajeros();
        List<Pasajero> lstPasajeros = pasajeroDatos.listarPasajeros();
        String datos[] = new String[5];
        
        for (int i=0;i<lstPasajeros.size();i++) {
            datos[0] = lstPasajeros.get(i).getNombre();
            datos[1] = lstPasajeros.get(i).getApellidoPat() + " " + lstPasajeros.get(i).getApellidoMat();            
            datos[2] = lstPasajeros.get(i).getNumeroDocumento();
            datos[3] = lstPasajeros.get(i).getNroTelefono1();
            datos[4] = lstPasajeros.get(i).getEstudiante();
            modeloTabla.addRow(datos);
        }
    }

