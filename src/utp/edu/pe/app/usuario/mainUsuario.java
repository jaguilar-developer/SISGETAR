    public void cargarUsuarios() {
        DefaultTableModel modeloTabla = (DefaultTableModel) tblUsuarios.getModel();
        modeloTabla.setRowCount(0);
        
        daoUsuarios usuarioDatos = new daoUsuarios();
        List<Usuario> lstUsuarios = usuarioDatos.listarUsuarios();
        String datos[] = new String[5];
        
        if(rolPasajero){
            for (int i=0;i<lstUsuarios.size();i++) {
                if(lstUsuarios.get(i).getNumeroDocumento().equals(lblNroDoc.getText())) {
                    datos[0] = lstUsuarios.get(i).getNombre();
                    datos[1] = lstUsuarios.get(i).getApellidoPat() + " " + lstUsuarios.get(i).getApellidoMat();            
                    datos[2] = lstUsuarios.get(i).getNumeroDocumento();
                    datos[3] = lstUsuarios.get(i).getUsuario();
                    datos[4] = lstUsuarios.get(i).getRol();
                    modeloTabla.addRow(datos);
                }
            }
            panelRestaurar.setVisible(false);
        } else {
            for (int i=0;i<lstUsuarios.size();i++) {
                datos[0] = lstUsuarios.get(i).getNombre();
                datos[1] = lstUsuarios.get(i).getApellidoPat() + " " + lstUsuarios.get(i).getApellidoMat();            
                datos[2] = lstUsuarios.get(i).getNumeroDocumento();
                datos[3] = lstUsuarios.get(i).getUsuario();
                datos[4] = lstUsuarios.get(i).getRol();
                modeloTabla.addRow(datos);
            }
            panelActualizar.setVisible(false);
        }
    }

