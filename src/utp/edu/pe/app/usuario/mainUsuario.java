    private void btnRestaurarPassMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRestaurarPassMouseClicked
        daoUsuarios usuarioDatos = new daoUsuarios();
        String msjRespuesta;
        
        int fila = tblUsuarios.getSelectedRow();
        String usuario = tblUsuarios.getValueAt(fila, 3).toString();
        
        int res;        
        res = JOptionPane.showConfirmDialog(this, "¿Desea restaurar la contraseña del usuario " + usuario + " ?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (res == 0) {
            msjRespuesta = usuarioDatos.restaurarUsuario(usuario);
            JOptionPane.showMessageDialog(this, msjRespuesta);        
        }
        
    }//GEN-LAST:event_btnRestaurarPassMouseClicked

